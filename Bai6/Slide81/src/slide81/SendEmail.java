    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        // TODO add your handling code here:
        final String username = txtUser.getText().trim();
        final String password = txtPassword.getText().trim();

        Properties prop = new Properties();
        //
        prop.put("mail.smtp.host", "smtp.outlook.com");
        //prop.put("mail.smtp.host", "smtp.gmail.com");
        prop.put("mail.smtp.port", "587");
        prop.put("mail.smtp.auth", "true");
        prop.put("mail.smtp.starttls.enable", "true"); //TLS
        
        javax.mail.Authenticator auth = new javax.mail.Authenticator() {
            //override the getPasswordAuthentication method
            protected PasswordAuthentication getPasswordAuthentication() {
                return new PasswordAuthentication(username, password);
            }
        };
        Session session = Session.getInstance(prop, auth);

        try {

            MimeMessage message = new MimeMessage(session);
            message.setFrom(new InternetAddress(txtUser.getText().trim()));
            message.setRecipients(
                    Message.RecipientType.TO,
                    InternetAddress.parse(txtTo.getText().trim())
            );
            message.setSubject(txtSubject.getText());
            
            
            MaHoaMD5 md = new MaHoaMD5();
            String maHoaMD5 = "";
            try {
                maHoaMD5 = md.converHashToString(txtMessage.getText());

            } catch (NoSuchAlgorithmException ex) {
                Logger.getLogger(SendEmail.class.getName()).log(Level.SEVERE, null, ex);
            }

            String NoiDung = "Mật khẩu: " + txtMessage.getText() + "\n\r"
                    + "Mật khẩu mã hóa MD5: " + maHoaMD5;
            
 
            message.setText(NoiDung);

            Transport.send(message);

            JOptionPane.showMessageDialog(null, "Gửi mail thành công.");

        } catch (MessagingException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }

    }//GEN-LAST:event_btnSendActionPerformed

