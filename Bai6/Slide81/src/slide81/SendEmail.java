    private void btnSendSSLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendSSLActionPerformed
        // TODO add your handling code here:
        final String username = txtUser.getText().trim();
        final String password = txtPassword.getText().trim();

        Properties prop = new Properties();
        //smtp.gmail.com
        prop.put("mail.smtp.host", "smtp.gmail.com");
        prop.put("mail.smtp.port", "465");
        prop.put("mail.smtp.auth", "true");
        prop.put("mail.smtp.socketFactory.port", "465");
        prop.put("mail.smtp.socketFactory.class", "javax.net.ssl.SSLSocketFactory");

        Session session = Session.getInstance(prop,
                new javax.mail.Authenticator() {
            protected PasswordAuthentication getPasswordAuthentication() {
                return new PasswordAuthentication(username, password);
            }
        });

        try {

            Message message = new MimeMessage(session);
            message.setFrom(new InternetAddress(txtUser.getText().trim()));
            message.setRecipients(
                    Message.RecipientType.TO,
                    InternetAddress.parse(txtTo.getText().trim())
            );
            message.setSubject(txtSubject.getText());
            message.setText(txtMessage.getText());

            Transport.send(message);

            JOptionPane.showMessageDialog(null, "Gửi mail thành công.");

        } catch (MessagingException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSendSSLActionPerformed

